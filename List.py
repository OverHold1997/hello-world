a=[5,7,2,6,8,9,2]
a1=[1,2,3,5]
#求列表元素个数
b=len(a)
print(b)
#求列表最大值
c=max(a)
print(c)
#求列表最小值
d=min(a)
print(d)
#在列表添加新的对象
a.append(3)
print(a)
#计算列表中重复出现的元素
f=list.count(a,2)
print(f)
#在一个列表末尾添加另一个列表的元素
a.extend(a1)
print(a)
#从列表找出某个值的位置
e=a.index(5)
print(e)
#将对象插入列表
a.insert(0,6)
print(a)
#移除列表中的某个元素（默认最后一个元素），并且返回该元素的值
b1=[2,5,6,7,8,9]
b1.pop(3)
print(b1)
#移除列表中某个值的第一个匹配项
b2=[5,6,8,7,9]
b2.remove(5)
print(b2)
#反向列表中元素
a3=[2,3,5,4,6]
a3.reverse()
print(a3)
#对原列表中的元素进行排序
a5=[5,2,3,6,48,12]
a5.sort()
print("升序：",a5)
a5.sort(reverse=True)
print("降序：",a5)
#清空列表
a4=[2,4,5,6,23]
a4.clear()
print(a4)
#复制列表
b2=[1,2,3,4]
b3=[5,6,7,8]
b3=b2.copy()
print(b3)